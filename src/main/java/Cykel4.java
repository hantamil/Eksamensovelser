public class Cykel4 {
    private String mærke;
    private String farve;
    private double pris;

    public Cykel4(String mærke, String farve, double pris) {
        this.mærke = mærke;
        this.farve = farve;
        this.pris = pris;
    }

    public String getMærke() {
        return mærke;
    }

    public String getFarve() {
        return farve;
    }

    public double getPris() {
        return pris;
    }
}
